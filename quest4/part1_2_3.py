def part3(parsed_input):
    if len(parsed_input) % 2 == 0:
        index_1 = len(parsed_input) // 2
        index_2 = len(parsed_input) // 2 - 1
        parsed_input_copy = copy.copy(parsed_input)

        picked_ele_1 = parsed_input.pop(index_1)
        picked_ele_2 = parsed_input_copy.pop(index_2)

        return min(
            sum([abs(nail_length - picked_ele_1) for nail_length in parsed_input]),
            sum([abs(nail_length - picked_ele_2) for nail_length in parsed_input_copy]),
        )
    else:
        index = len(parsed_input) // 2
        picked_ele = parsed_input.pop(index)
        return sum([abs(nail_length - picked_ele) for nail_length in parsed_input])


